
public class Tuple {
	char caract;
	int freq;
	
	public Tuple(Character caract, int freq) {
		super();
		this.caract = caract;
		this.freq = freq;
	}
	
	
	public char getCaract() {
		return caract;
	}


	public void setCaract(char caract) {
		this.caract = caract;
	}


	public int getFreq() {
		return freq;
	}
	public void setFreq(int freq) {
		this.freq = freq;
	}
	

}
